package it.eng.tz.puglia.aet.paesaggio.presentazioneistanza.servizi_esterni.protocollo;

import java.io.File;
import java.util.Date;

public class GeneratedFileBean {

	private long id;
	private String name;
	private String description;
	private Date data;
	private String mimeType;
	private byte[] content;
	private String filePath;
	private String checksum;
	private boolean deleted;
	private String inputProtocolNumber;
	private String inputProtocolDate;
	private String outputProtocolNumber;
	private String outputProtocolDate;
	private File file;
	private String alfrescoPath;

	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Date getData() {
		return data;
	}
	public void setData(Date data) {
		this.data = data;
	}
	public String getMimeType() {
		return mimeType;
	}
	public void setMimeType(String mimeType) {
		this.mimeType = mimeType;
	}
	public byte[] getContent() {
		return content;
	}
	public void setContent(byte[] content) {
		this.content = content;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getChecksum() {
		return checksum;
	}
	public void setChecksum(String checksum) {
		this.checksum = checksum;
	}
	public boolean isDeleted() {
		return deleted;
	}
	public void setDeleted(boolean deleted) {
		this.deleted = deleted;
	}
	public String getInputProtocolNumber() {
		return inputProtocolNumber;
	}
	public void setInputProtocolNumber(String inputProtocolNumber) {
		this.inputProtocolNumber = inputProtocolNumber;
	}
	public String getInputProtocolDate() {
		return inputProtocolDate;
	}
	public void setInputProtocolDate(String inputProtocolDate) {
		this.inputProtocolDate = inputProtocolDate;
	}
	public String getOutputProtocolNumber() {
		return outputProtocolNumber;
	}
	public void setOutputProtocolNumber(String outputProtocolNumber) {
		this.outputProtocolNumber = outputProtocolNumber;
	}
	public String getOutputProtocolDate() {
		return outputProtocolDate;
	}
	public void setOutputProtocolDate(String outputProtocolDate) {
		this.outputProtocolDate = outputProtocolDate;
	}
	public File getFile() {
		return file;
	}
	public void setFile(File file) {
		this.file = file;
	}
	public String getAlfrescoPath() {
		return alfrescoPath;
	}
	public void setAlfrescoPath(String alfrescoPath) {
		this.alfrescoPath = alfrescoPath;
	}
}
